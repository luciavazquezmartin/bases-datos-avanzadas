-- 3. Estrategia PL/SQL: Generación masiva de 500 préstamos
BEGIN
    FOR i IN 1..500 LOOP
        INSERT INTO prestamos (dni_lector, isbn_libro, fecha_prestamo) 
        VALUES (
            '12345678A', 
            CASE ROUND(DBMS_RANDOM.VALUE(1,2)) WHEN 1 THEN '978-84' ELSE '978-99' END,
            SYSDATE - ROUND(DBMS_RANDOM.VALUE(1, 30))
        );
    END LOOP;
    COMMIT;
END;
/

