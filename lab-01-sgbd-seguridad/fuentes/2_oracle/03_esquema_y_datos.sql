-- Cambio a la base de datos enchufable
ALTER SESSION SET CONTAINER = XEPDB1;

-- 0. Limpieza previa de tablas simulando "IF EXISTS" (CASCADE CONSTRAINTS borra también las dependencias)
BEGIN EXECUTE IMMEDIATE 'DROP TABLE prestamos CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE libros CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE lectores CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE productos CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/

-- 1. Definición del esquema
CREATE TABLE lectores (dni VARCHAR2(15) PRIMARY KEY, nombre VARCHAR2(50));
CREATE TABLE libros (isbn VARCHAR2(20) PRIMARY KEY, titulo VARCHAR2(100));
CREATE TABLE prestamos (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dni_lector VARCHAR2(15) REFERENCES lectores(dni),
    isbn_libro VARCHAR2(20) REFERENCES libros(isbn),
    fecha_prestamo DATE
);
CREATE TABLE productos (id NUMBER, nombre VARCHAR2(50));

